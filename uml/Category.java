public class Category {

	private string name;
	private string slug;

	public string getName() {
		return this.name;
	}

	/**
	 * 
	 * @param name
	 */
	public void setName(string name) {
		this.name = name;
	}

	public string getSlug() {
		return this.slug;
	}

	/**
	 * 
	 * @param slug
	 */
	public void setSlug(string slug) {
		this.slug = slug;
	}

}