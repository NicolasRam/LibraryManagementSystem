public class Location {

	private int level;
	private BookType type;

	public int getLevel() {
		return this.level;
	}

	/**
	 * 
	 * @param level
	 */
	public void setLevel(int level) {
		this.level = level;
	}

	public BookType getType() {
		return this.type;
	}

	/**
	 * 
	 * @param type
	 */
	public void setType(BookType type) {
		this.type = type;
	}

}